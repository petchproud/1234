def calculate_net_salary(hours_worked, hours_rate, additional_income, deductions):
    NORMAL_HOURS = 160
    OT_RATE_MULTIPLIER = 1.5
    if hours_worked <= NORMAL_HOURS:
        # ‡πÑ‡∏°‡πà‡∏°‡∏µ OT
        base_salary = hours_worked * hours_rate
        ot_hours = 0
    else:
        normal_pay = NORMAL_HOURS * hours_rate
        ot_hours = hours_worked - NORMAL_HOURS
        ot_rate = hours_rate * OT_RATE_MULTIPLIER
        ot_pay = ot_hours * ot_rate
        base_salary = normal_pay + ot_pay
    
    # 2. ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏£‡∏≤‡∏¢‡πÑ‡∏î‡πâ‡∏£‡∏ß‡∏°‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î (Total Income)
    total_income = base_salary + additional_income
    
    # 3. ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡πÄ‡∏á‡∏¥‡∏ô‡πÄ‡∏î‡∏∑‡∏≠‡∏ô‡∏™‡∏∏‡∏ó‡∏ò‡∏¥ (Net Salary)
    net_salary = total_income - deductions
    
    ot_rate_used = hours_rate * OT_RATE_MULTIPLIER

    return net_salary, total_income, base_salary, ot_hours, ot_rate_used

